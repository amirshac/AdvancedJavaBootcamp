package VendingMachine.VendingMachine;

public class HotCappuccino extends HotEspresso{
	
	private static String DEF_NAME = "Hot Cappucinno";
	private static int DEF_TOTAL_VOLUME = 200;
	private static int ESPRESSO_SHOTS = 2;
	
	public HotCappuccino() {
		super(ESPRESSO_SHOTS);
		setName(DEF_NAME);
		setMilk(DEF_TOTAL_VOLUME - this.volume);
	}

}
